export class Employee {
    id: number;
    firstName: string;
    lastName: string;
    emailId: string;
    designation: string;
    
    constructor(id: number, firstName: string, lastName: string, emailId: string, designation: string) {
      this.id = id;
      this.firstName = firstName;
      this.lastName = lastName;
      this.emailId = emailId;
      this.designation = designation;
    }
 
  }
  
